class FoodItem {
  final String name;
  final double calories;
  final double fats;
  final double proteins;
  final double carbs;
  final double sugar;
  final double fiber;
  final double sodium;
  final DateTime date;

  FoodItem({
    required this.name,
    required this.calories,
    required this.fats,
    required this.proteins,
    required this.carbs,
    required this.sugar,
    required this.fiber,
    required this.sodium,
    required this.date,
  });
}
